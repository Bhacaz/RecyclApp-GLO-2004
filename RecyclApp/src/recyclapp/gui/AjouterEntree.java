    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel model =  (DefaultTableModel) this.ProduitsEntree.getModel();
        int[] rows = ProduitsEntree.getSelectedRows();
        for(int row : rows){
            model.removeRow(ProduitsEntree.convertRowIndexToModel(row));
        }
        ProduitsEntree.clearSelection();
    }//GEN-LAST:event_jButton2ActionPerformed

