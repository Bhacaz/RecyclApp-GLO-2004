    private void BoutonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BoutonOKActionPerformed
        boolean tableValide = true;
        DefaultTableModel model =  (DefaultTableModel) this.ProduitsEntree.getModel();
        panier = new Panier();
        
        for(int i=0;i<model.getRowCount();i++)
        {
            if (Integer.parseInt(model.getValueAt(i, 1).toString()) <= 0) {tableValide = false;}
            if (model.getValueAt(i, 0).toString().isEmpty() == true) {tableValide = false;}
            Produit monProduit = new Produit();
            monProduit.asgNomProduit(model.getValueAt(i, 0).toString());
            monProduit.asgQuantiteProduit(Integer.parseInt(model.getValueAt(i, 1).toString()));
            panier.ajouterProduit(monProduit);
        }
        if(NomEntree.getText().isEmpty()==false && tableValide == true) {
            nom = NomEntree.getText();
            description = DescriptionEntree.getText();
            couleur = ApercuCouleur.getBackground();
            this.setVisible(false);
        }
        else{
            BoutonOK.setToolTipText("L'entrée  doit posséder un nom, et chaque produit doit posséder un nom et une quantité valide");
        }
    }//GEN-LAST:event_BoutonOKActionPerformed

